// Embeds schemas/openapi.json into a TypeScript module so the OpenAPI
// document is available at runtime without shipping the schemas directory.
// Invoked by `pnpm run generate` (see package.json).
import { readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const schemaPath = resolve(packageRoot, "../../../schemas/openapi.json");
const outputPath = resolve(packageRoot, "src/__generated__/document.ts");

// Round-trip through JSON.parse/stringify to normalize formatting and fail
// fast on malformed input.
const documentJson = JSON.stringify(
  JSON.parse(readFileSync(schemaPath, "utf8"))
);

const moduleSource = `/**
 * This file was auto-generated by scripts/embed-openapi-document.mjs from
 * schemas/openapi.json. Do not make direct changes to the file.
 */

/**
 * The Phoenix server's OpenAPI document, embedded as a parsed JSON value.
 * The document is intentionally typed loosely; it is consumed by
 * \`@mswjs/source\`, which validates it at runtime.
 */
export const phoenixOpenApiDocument: Record<string, unknown> = JSON.parse(
  ${JSON.stringify(documentJson)}
);
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, moduleSource);
